# Data Types
# --------------

# 1) Numeric data type :

# integer:
roll_no=10
print("roll no:",roll_no ,type(roll_no))


# float:
weight=55.5
print("weight:",weight,type(weight))

# complex:
a=10+3j
print("a:",a,type(a))

# 2) String data Type:
name="Sandra"
print("name:",name,type(name))

# 3) Boolean data type::
a=True
b=False
print("a:",a,type(a))
print('b:',b,type(b))

# 4) Sequence data type:

# list:
list=["Jibin","Sandra","Swapna"]
print("list:",list,type(list))

# tuple:
tuple=("Jibin","Sandra","Swapna")
print("tuple:",tuple,type(tuple))

# 5)Set Data Type:
name={"anu","shyam","david"}
print("name:",name,type(name))

# 6) Mapping Data Type:
data={'name':'Jibin'}
print("data",data,type(data))

